       private boolean validateEmail(){
            String emailPatternString = "^[_A-Za-z0-9\\+]+(\\.[_A-Za-z0-9-\\+]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
            Pattern emailPattern = Pattern.compile(emailPatternString);
            Matcher emailMatcher = emailPattern.matcher(txtEmailFeedback.getText());
            if(emailMatcher.matches()){
                return true;
            }
            else{
                lblNotification.setForeground(Color.red);
                txtEmailFeedback.setForeground(Color.red);
                lblNotification.setText("Please enter correct email");
                
               return false;
            }
    }
