    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!txtEmailFeedback.getText().equals("") &&!txtaReview.getText().equals("")){
            
           if(validateEmail()){
            lblNotification.setForeground(Color.green);   
            new Mail().mail(txtEmailFeedback.getText(),txtaReview.getText());
            lblNotification.setText("Thank you!We will look forward to you suggestions :)");
            lblEmailError.setText("");
            clearFields();
           }
        }
        else if(txtEmailFeedback.getText().equals("") &&txtaReview.getText().equals("")){
            lblEmailError.setForeground(Color.red);
            lblEmailError.setText("Some Fields Are Empty");
            txtEmailFeedback.setForeground(f3f7f8);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

